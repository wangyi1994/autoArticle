package com.example.autoarticle.model;

public class talkBean {
    /**
     * 该段对话的角色
     */
    private String role;
    /**
     * 该段对话的内容
     */
    private String text;

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
